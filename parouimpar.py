#lê um numero inteiro na entrada
num = int(input("Informe um numero inteiro: ")) 
#verifica se o numero é par atraves do resto da divisão
if num % 2 == 0:
#exibe a mensagem "par" caso seja par
    print("Par")
#se não, qualquer outra caso irá mostrar a mensagem "impar"
else:
    print ("impar")